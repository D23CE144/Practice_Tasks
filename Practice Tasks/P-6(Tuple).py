# Define a tuple
my_tuple = (10, 20, 30, 40, 50)

# Find maximum
max_value_tuple = max(my_tuple)

# Find minimum
min_value_tuple = min(my_tuple)

# Find length
length_tuple = len(my_tuple)

# Find sum
sum_tuple = sum(my_tuple)

# Print the results
print("Tuple:", my_tuple)
print("Max value in tuple:", max_value_tuple)
print("Min value in tuple:", min_value_tuple)
print("Length of tuple:", length_tuple)
print("Sum of tuple elements:", sum_tuple)
