# Define a list
my_list = [5, 15, 25, 35, 45]

# Find maximum
max_value_list = max(my_list)

# Find minimum
min_value_list = min(my_list)

# Find length
length_list = len(my_list)

# Find sum
sum_list = sum(my_list)

# Print the results
print("List:", my_list)
print("Max value in list:", max_value_list)
print("Min value in list:", min_value_list)
print("Length of list:", length_list)
print("Sum of list elements:", sum_list)
