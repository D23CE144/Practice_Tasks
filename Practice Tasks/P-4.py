# Create a tuple
my_tuple = (10, 20, 30, 40, 50)

# Convert tuple to string
tuple_str = str(my_tuple)

# Create a list
my_list = [16, 17, 81, 19, 12]

# Convert list to string
list_str = str(my_list)

# Create a set
my_set = {10, 11, 18, 61, 63}

# Convert set to string
set_str = str(my_set)

# Print the converted strings
print("Tuple as string:", tuple_str)
print("List as string:", list_str)
print("Set as string:", set_str)
