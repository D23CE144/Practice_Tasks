# Define the original string
my_string = "Hello, World! This is a Test String."

# Convert all characters to uppercase
upper_case_string = my_string.upper()

# Convert all characters to lowercase
lower_case_string = my_string.lower()

# Split the string by spaces
split_by_space = my_string.split()

# Split the string by commas
split_by_comma = my_string.split(',')

# Split the string by 'is'
split_by_is = my_string.split('is')

# Print the results
print("Original string:", my_string)
print("Uppercase string:", upper_case_string)
print("Lowercase string:", lower_case_string)
print("Split by space:", split_by_space)
print("Split by comma:", split_by_comma)
print("Split by 'is':", split_by_is)
